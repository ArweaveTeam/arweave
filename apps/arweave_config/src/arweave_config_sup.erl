-module(arweave_config_sup).
-export([start_link/0]).
-export([init/1]).

%%--------------------------------------------------------------------
%% @doc
%% @end
%%--------------------------------------------------------------------
start_link() ->
	supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%%--------------------------------------------------------------------
%% @hidden
%%--------------------------------------------------------------------
init(_Args) ->
	{ok, {supervisor(), children()}}.

%%--------------------------------------------------------------------
%% @hidden
%%--------------------------------------------------------------------
supervisor() ->
	#{
		strategy => one_for_all
	 }.

%%--------------------------------------------------------------------
%% @hidden
%%--------------------------------------------------------------------
children() ->
	[].
